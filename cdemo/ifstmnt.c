#include <stdio.h>

int main()
{
  int a = 0;

  // if statement to test is a is equal to 0
  if (a == 0)
  {
    printf("a is 0\n");
  }
  if (a != 0) {
    printf("a doesn't equal 0");
  }
  if (a > 0 ) {
    printf("a is greater than 0");
  }
  if (a >= 0) {
    printf("a is greater than or equal to 0");
  }
}
